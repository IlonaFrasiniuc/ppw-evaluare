let numar = parseInt(prompt("Introduceti un numar intreg de la 0 la 9:"));

switch (numar) {
    case 0:
        alert("zero");
        break;
    case 1:
        alert("unu");
        break;
    case 2:
        alert("doi");
        break;
    case 3:
        alert("trei");
        break;
    case 4:
        alert("patru");
        break;
    case 5:
        alert("cinci");
        break;
    case 6:
        alert("sase");
        break;
    case 7:
        alert("sapte");
        break;
    case 8:
        alert("opt");
        break;
    case 9:
        alert("noua");
        break;
    default:
        alert("Numarul introdus nu se incadreaza intre 0 si 9.");
        break;
}